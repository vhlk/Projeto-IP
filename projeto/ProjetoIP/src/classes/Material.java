package classes;

public class Material {
	private String titulo;
	private String tipo;
	
	public Material() {
		this.titulo = null;
		this.tipo = null;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}
